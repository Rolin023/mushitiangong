let currentStoryQueue = [];
let currentStoryIndex = 0;
let onStoryComplete = null;

let isTyping = false;
let typingTimer = null;
let fullText = "";
let currentTypeIndex = 0;

function playStory(storyKey, callback) {
    currentStoryQueue = scriptData[storyKey];
    if (!currentStoryQueue || currentStoryQueue.length === 0) {
        if (callback) callback();
        return;
    }

    currentStoryIndex = 0;
    onStoryComplete = callback;

    document.getElementById('story-overlay').classList.remove('hidden');
    const npcUI = document.getElementById('npc-lu-shifu');
    if(npcUI) npcUI.classList.add('hidden');
    renderCurrentDialog();
}

function playDynamicStory(dialogArray, callback) {
    currentStoryQueue = dialogArray;
    currentStoryIndex = 0;
    onStoryComplete = callback;

    document.getElementById('story-overlay').classList.remove('hidden');
    const npcUI = document.getElementById('npc-lu-shifu');
    if(npcUI) npcUI.classList.add('hidden');
    renderCurrentDialog();
}

function renderCurrentDialog() {
    const dialog = currentStoryQueue[currentStoryIndex];
    document.getElementById('story-name').innerText = dialog.name;

    const overlay = document.getElementById('story-overlay');
    if (dialog.bg) {
        overlay.style.backgroundImage = `url('${dialog.bg}')`;
        overlay.style.backgroundColor = "rgba(0, 0, 0, 0.4)";
    } else {
        overlay.style.backgroundImage = 'none';
        overlay.style.backgroundColor = "rgba(244, 232, 210, 0.95)";
    }

    const avatarImg = document.getElementById('story-avatar');
    if (dialog.avatar) {
        avatarImg.src = dialog.avatar;
        avatarImg.onerror = () => { avatarImg.classList.add('hidden'); };
        avatarImg.classList.add('show');
        avatarImg.classList.remove('hidden');
    } else {
        avatarImg.classList.remove('show');
        setTimeout(() => avatarImg.classList.add('hidden'), 300);
    }

    const textEl = document.getElementById('story-text');
    textEl.innerText = "";
    fullText = dialog.text;
    isTyping = true;
    currentTypeIndex = 0;
    
    if (typingTimer) clearTimeout(typingTimer);
    
    typeNextChar();
}

function typeNextChar() {
    if (currentTypeIndex < fullText.length) {
        currentTypeIndex++;
        document.getElementById('story-text').innerText = fullText.substring(0, currentTypeIndex);
        typingTimer = setTimeout(typeNextChar, 40); 
    } else {
        isTyping = false; 
    }
}

function advanceStory() {
    if (typeof resumeBgmIfNeeded === 'function') {
        resumeBgmIfNeeded();
    }

    if (isTyping) {
        if (typingTimer) clearTimeout(typingTimer);

        document.getElementById('story-text').innerText = fullText;

        isTyping = false;

        return;
    }

    currentStoryIndex++;

    if (currentStoryIndex < currentStoryQueue.length) {
        renderCurrentDialog();
    } else {
        if (typeof closeMusicSettingsPanel === 'function') {
            closeMusicSettingsPanel();
        }

        document.getElementById('story-overlay').classList.add('hidden');
        
        if (onStoryComplete) onStoryComplete();
        
        const npcUI = document.getElementById('npc-lu-shifu');
        const introActive = document.getElementById('scene-intro') && document.getElementById('scene-intro').classList.contains('active');
        const outroActive = document.getElementById('scene-outro') && document.getElementById('scene-outro').classList.contains('active');
        
        if (npcUI) {
            if (introActive || outroActive) {
                npcUI.classList.add('hidden');
            } else {
                npcUI.classList.remove('hidden');
            }
        }
    }
}